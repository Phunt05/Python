import math

# Arithmetic
print(2 + 2)  # 4
print(2 - 2)  # 0
print(2 * 2)  # 4
print(2 ** 3)  # 8(2^3)
print(2 / 2)  # 1.0(Decimal)
print(2 // 2)  # 1(Integer)
print(10 % 3)  # 1(Reminder)

# Assignment
x = 10
x += 3
print(x)  # 13

# Methods
x = -2.7
print(round(x))  # -3
print(abs(x))  # 2.7
print(math.ceil(x))  # -2
print(math.floor(x))  # -3
